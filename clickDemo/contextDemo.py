import click

word = '''
我这人比较nice，喜欢直来直去，我知道你技术不错，否则当时也不会招你进来，你可能会觉得$biz挺无聊的，但最好不要有排斥心理。我们的团队在做事情，而且有事可做，作为我重点培养的对象，你因该要学会感恩。我一直在努力给你们创造更好的工作环境，如果你遇到什么困难，找我，我帮你协调。但你从来没有找过我，或者换句话说没有融入这个集体，在这个家庭里，每个人都是你的老师，不要搞的太孤立。

其实，我对你是有一些失望的。当初给你$level，是高于你面试时的水平的。我是希望进来后，你能够拼一把，快速成长起来的。$cur_level这个层级，不是把事情做好就可以的。你需要有体系化思考的能力。你做的事情，他的价值点在哪里?你是否作出了壁垒形成了核心竞争力?你做的事情，和公司内其他团队的差异化在哪里?你的事情，是否沉淀了一套可复用的物理资料和方法论?为什么是你来做，其他人不能做吗?你需要有自己的判断力，而不是我说什么你就做什么。我不需要中规中矩的答卷，它是有一些差异化在里面的。后续，把你的思考沉淀到日报周报月报里，我希望看到你的思考，而不仅仅是进度。另外，提醒一下，你的产出，和同层级比，是有些单薄的，马上要到年底了，加把劲儿。你看咱们团队的$other去年晋升之前，可以一整年都在项目室打地铺的。成长，一定是伴随着痛苦的，当你最痛苦的时候其实才是你成长最快的时候。

我们的业务在飞速发展，说实话公司内外有很多双眼睛盯着这块蛋糕，3年内100倍的增长是保守估计。我自己还好说已经财富自由了，但你呢？部门给了你这么好的平台，不拿出一点不一样的成就，是无法说服大家的。

公司的文化是团队合作，拥抱变化，我觉得你是时候做出一点改变了。公司没有超人，包括我也一样，不要总想着自己是与众不同的，你需要找好自己的位置。只有认清了自己，你才能有相应的举措改善，否则连你自己都觉得没有依着感。来年3月份还会有一次答辩，多思考思考我说的话，你就会获得成长。没有提升，对你、对公司都没有什么好处，这些差距你需要搞得明明白白。

C今年就由你勉强承接一下，我希望你能总结经验教训，让自己的感情软着陆，沉下心来做出一些改变，明年的A就是你的了。你应该知道，在我的上面当领导的，没有弱干部。给你C，我自己也是感觉脸上无光。我希望你接下来的工作能够证明我的眼光，给我一个交代，也给自己一个交代。
'''


@click.group(context_settings=dict(help_option_names=['-h', '--help']))
@click.option('--debug')
@click.pass_context
def run(ctx, debug):
    print(ctx.obj)
    click.echo('2', debug)


@click.command()
@click.option('--name')
@click.option('--biz')
@click.option('--level')
@click.option('--cur_level')
@click.option('--other')
def sync(name, biz, level, cur_level, other):
    click.echo(word.replace("$name", name).replace("$biz", biz).replace("$level", level).replace("$cur_level",
                                                                                                 cur_level).replace(
        "$other", other))


if __name__ == '__main__':
    run.add_command(sync)
    run()
